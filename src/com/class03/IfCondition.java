public class IfCondition {

	public static void main(String[] args) {
		
		
		int a=10;
		int b=70;
		
		if (a>b ) {// if true
		
		System.out.println(" a is larger than b");
		}else { // if false(otherwise)
			System.out.println("b is larger than a");
		}

		int temp=35;
		
		if (temp>30) {
		System.out.println("I am going for a walk"); // !!!only works if it is true!!!
		}else {
			System.out.println(" I stay at home"); // !!! otherwise or if its false!!!
		}
		
		
		
		
		
		
		
		
		
		
		
		
	}
}