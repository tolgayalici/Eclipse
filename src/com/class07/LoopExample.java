package com.class07;

public class LoopExample {

	public static void main(String[] args) {
		
		/*
		 * print even numbers from 20 to 100
		 */
		
		
		int e=20;
		
		while (e<=100) {
			System.out.print(e+ " ");
			e+=2;
		}
		
		System.out.println();
		System.out.println("---------------");
		
		int a=20;
		
		while(a<=100) {
			
			if (a%2==0) {
				System.out.print(a+ " ");
			}

			a++;
		}
		
	
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
