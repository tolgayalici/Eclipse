package ReviewClass04;

public class WhileLoopWithBreakContinue {

	public static void main(String[] args) {
		
		
	int i=10;
	while(i<15) {
		System.out.println("Infinite");
		
		if(i<15) {
			continue;
		}else {
			System.out.println("Hi");
		}
		i++;
	}
		
		
		int a=10;
		while(a<15) {
			System.out.println("Infinite");
			
			if(a<15) {
				break;
			}else {
				System.out.println("Hi");
			}
			a++;
		}
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
