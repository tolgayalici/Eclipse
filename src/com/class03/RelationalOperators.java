package com.class03;

public class RelationalOperators {

	public static void main(String[] args) {
		
		
		int a=10;
		int b=7;
		System.out.println(a>b);//true  > larger
		System.out.println(a<b);//false  < smaller
		System.out.println(a==b);//false == equal
		System.out.println(a!=b);//true  != not equal
		
		boolean result=a>=b;
		boolean result1=a<=b;
		System.out.println(result);//true
		System.out.println(result1);//false
		
		int c=20;
		int d=29;
		System.out.println(c==d);//checking equality
		System.out.println(c=d);//assigning
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}