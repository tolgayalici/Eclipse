package com.class07;

public class WhileVsDoWhile {

	public static void main(String[] args) {

		int num = 1;

		do {

			System.out.println(num);
			num++;

		} while (num <= 5);

		/*
		 * while= first check then execute do= first execute then check Even when
		 * condition is false Do will execute your code AT LEST ONCE
		 */

	}
}





















